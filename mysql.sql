
CREATE TABLE CUSTOMER(C_ID INT NOT NULL AUTO_INCREMENT, LAST CHAR(15) NOT NULL, FIRST CHAR(20) NOT NULL, MI CHAR(1), DOB DATE NOT NULL, ADDRESS CHAR(20) NOT NULL, CITY CHAR(15) NOT NULL, STATE CHAR(2) NOT NULL, ZIP INT NOT NULL, DPHONE CHAR(13) NOT NULL, EMAIL CHAR(25) NOT NULL, USER_ID CHAR(20) NOT NULL, PASS CHAR(20) NOT NULL, PRIMARY KEY(C_ID));

CREATE TABLE EMPLOYEE(E_ID INT NOT NULL, ROLE CHAR(2) NOT NULL, ID CHAR(20) NOT NULL, PASSWORD CHAR(20) NOT NULL, EMAIL_ CHAR(30) NOT NULL, PRIMARY KEY(E_ID));

CREATE TABLE ORDERSOURCE(OS_ID INT NOT NULL, OS_DESC CHAR(255) NOT NULL, PRIMARY KEY(OS_ID));

CREATE TABLE SHIPMENTS(SHIP_ID INT NOT NULL, SHIP_DATE_EXPECTED CHAR(10) NOT NULL, PRIMARY KEY(SHIP_ID));

CREATE TABLE ORDERS(O_ID INT NOT NULL, O_DATE CHAR(10) NOT NULL, METHPMT CHAR(5) NOT NULL, C_ID INT NOT NULL, OS_ID INT NOT NULL, PRIMARY KEY(O_ID), FOREIGN KEY(C_ID) REFERENCES CUSTOMER(C_ID), FOREIGN KEY(OS_ID) REFERENCES ORDERSOURCE(OS_ID));

CREATE TABLE CATEGORY(CAT_ID INT NOT NULL, CAT_DESC CHAR(255) NOT NULL, PRIMARY KEY(CAT_ID));

CREATE TABLE ITEM(ITEM_ID INT NOT NULL, ITEM_DESC CHAR(255) NOT NULL, CAT_ID INT NOT NULL, ITEM_IMAGE CHAR(30) NOT NULL, PRIMARY KEY(ITEM_ID), FOREIGN KEY(CAT_ID) REFERENCES CATEGORY(CAT_ID));

CREATE TABLE ORDER_LINE(O_ID INT NOT NULL, INV_ID INT NOT NULL, OL_QUANTITY INT NOT NULL, OL_PRICE INT NOT NULL, FOREIGN KEY(O_ID) REFERENCES ORDERS(O_ID), FOREIGN KEY(INV_ID) REFERENCES INVENTORY(INV_ID), PRIMARY KEY(O_ID,INV_ID));

CREATE TABLE INVENTORY(INV_ID INT NOT NULL, ITEM_ID INT NOT NULL, COLOR CHAR(20) NOT NULL, INV_SIZE CHAR(2), INV_PRICE REAL NOT NULL, INV_QOH INT NOT NULL, PRIMARY KEY(INV_ID), FOREIGN KEY(ITEM_ID) REFERENCES ITEM(ITEM_ID));

CREATE TABLE SHIPMENT_LINE(SHIP_ID INT NOT NULL, INV_ID INT NOT NULL, SL_QUANTITY INT NOT NULL, SHIP_DATE_RECEIVED CHAR(10), PRIMARY KEY(SHIP_ID,INV_ID), FOREIGN KEY(SHIP_ID) REFERENCES SHIPMENTS(SHIP_ID), FOREIGN KEY(INV_ID) REFERENCES INVENTORY(INV_ID));

